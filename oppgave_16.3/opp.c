#include <stdio.h>

int x; /* глобальна зміна х=0 */

int main(void)
{
    int *px = &x; /* вказує на глоабьну зміну, бо локальна зміна ще не створена */
    int x = 7;    /* оголошення локальної змінної х=7 */

    *px = 4;               /* рх має наприямок до глобальної змінної х, тому значення міняється у локальної хмінній х=4*/
    printf("x = %d\n", x); /* x = 7 , бо посилаться на локальну зміну*/
    return 0;
}
